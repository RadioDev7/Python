# Кортежи похожи на списки, но в них нельзя менять значе-ния. 
# Начальные значения остаются до конца выполнения программы. 

months = ("Jan", "Feb", "Mar", "Apr", "May",  "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec")
print(months[1]) # Начало списка
print(months[-1]) # Конец списка
print(months) # Вывод всего списка