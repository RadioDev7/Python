# Есть еще несколько дополнительных операторов присваивания. К ним относятся такие операторы, 
# +=, -=, *=, /=, //=, **=
# Пример
a = 15
b = 20
 
a += b
print(a)

# Это помогает сокращать код
# Остальные решения
a -= b
print(a)

a *= b
print(a)

a /= b
print(a)

a //= b
print(a)

a **= b
print(a)

a %= b
print(a)